    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)tab_eve.getModel();
       //String empty = ser_eve.getText();
       if (model.getRowCount() != 0){
            model.setRowCount(0);
        }
       
        List<Sport_Event> sList = new ArrayList<Sport_Event>();   
        
        View_Sports_Events p =new  View_Sports_Events();
        
         try {
            sList = p.View_spo();
          
            for (int i=0;i<sList.size();i++){
            model.addRow(new Object[]{});
            tab_eve.setValueAt(sList.get(i).getSp_ID(), i, 0);
            tab_eve.setValueAt(sList.get(i).getSp_Name(), i, 1);
            tab_eve.setValueAt(sList.get(i).getSp_Percentage(), i, 2);
            
            tab_eve.setValueAt(sList.get(i).getSp_Status(), i, 3);
        }
       
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

