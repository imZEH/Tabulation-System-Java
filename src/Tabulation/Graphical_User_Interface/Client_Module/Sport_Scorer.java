    private void ser_veKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ser_veKeyPressed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel) tab_eve.getModel();
       String empty = ser_ve.getText();
       
       if (model.getRowCount() != 0){
            model.setRowCount(0);
        }
       
       List<Sport_Event> sList = new ArrayList<Sport_Event>();   
        
        View_Sports_Events p =new  View_Sports_Events();
        
         try {
            sList = p.View_spo_ser(empty, empty, empty, empty, empty);
          
            for (int i=0;i<sList.size();i++){
            model.addRow(new Object[]{});
            tab_eve.setValueAt(sList.get(i).getSp_ID(), i, 0);
            tab_eve.setValueAt(sList.get(i).getSp_Name(), i, 1);
            tab_eve.setValueAt(sList.get(i).getSp_Percentage(), i, 2);
            tab_eve.setValueAt(sList.get(i).getSp_Rounds(), i, 3);
            tab_eve.setValueAt(sList.get(i).getSp_Status(), i, 4);
        }
        if(empty.isEmpty()){
        model.setRowCount(0);
        }
         } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_ser_veKeyPressed

    private void tab_eveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_eveMouseClicked
        // TODO add your handling code here:
        row = tab_eve.getSelectedRow();
         DefaultTableModel model=(DefaultTableModel) tab_parts.getModel();
       String empty = ser_ve.getText();
       String TableClick = (tab_eve.getModel().getValueAt(row, 0).toString());
       spid.setText(TableClick);
       if (model.getRowCount() != 0){
            model.setRowCount(0);
        }
       
       List<Participant> sList = new ArrayList<Participant>();   
        
        View_Parti p =new  View_Parti();
        
         try {
            sList = p.queryViewParti_Sports(TableClick);
          
            for (int i=0;i<sList.size();i++){
            model.addRow(new Object[]{});
            tab_parts.setValueAt(sList.get(i).getId(), i, 0);
            tab_parts.setValueAt(sList.get(i).getFullname(), i, 1);
            tab_parts.setValueAt(sList.get(i).getTeam(), i, 2);
           
        }
        if(empty.isEmpty()){
        model.setRowCount(0);
        }
         } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_tab_eveMouseClicked

