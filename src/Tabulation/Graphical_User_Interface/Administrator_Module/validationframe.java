    public List<Voters_Password>querybatch()throws Exception{
        List CollList = new ArrayList();
        con =  (java.sql.Connection) Tabulation.Connections.Conn.getMySqlConnection();
        java.sql.Statement stmt = con.createStatement();
        rs = stmt.executeQuery("Select * from voters_password Where Vp_Printed = 'NP' GROUP BY Vp_batchname ");
        
        while(rs.next()){
            String EventName = rs.getString("Vp_batchname");
            Voters_Password ac = new Voters_Password(EventName);
            CollList.add(ac);
            
        }
        return CollList;
    }
    
    
    
