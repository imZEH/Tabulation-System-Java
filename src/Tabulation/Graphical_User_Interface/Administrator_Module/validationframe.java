     public List<Voters_Password>querypass(String btchname)throws Exception{
        List CollList = new ArrayList();
        con =  (java.sql.Connection) Tabulation.Connections.Conn.getMySqlConnection();
        java.sql.Statement stmt = con.createStatement();
        rs = stmt.executeQuery("Select * from voters_password Where Vp_Printed = 'NP' and Vp_batchname = '"+btchname+"'");
        
        while(rs.next()){
            String pass = rs.getString("VP_Pass");
            String datecre = rs.getString("VP_DateCreated");
            String dateexpi = rs.getString("VP_Expired");
            String status = rs.getString("Vp_Status");
            String pStatus = rs.getString("Vp_Printed");
            String btcname = rs.getString("Vp_batchname");
            Voters_Password ac = new Voters_Password(pass,datecre,dateexpi,status,pStatus,btcname);
            CollList.add(ac);
            
        }
        return CollList;
    }
    
   
    
    
    
   
    
