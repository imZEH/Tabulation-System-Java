    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DateFormat df = new SimpleDateFormat("MMMMM dd,yyyy");
        Date Date = date.getDate();
        String getdate= df.format(Date);
        String Sched = ""+getdate+" "+Hour.getText()+":"+Min.getText()+" "+(String)AMPM.getSelectedItem();
        if(SPname.getText().equals("")||SPtype.getSelectedItem().equals("--Select--Type--:")){
            JOptionPane.showMessageDialog(null,"Please Check all fields","Error",JOptionPane.ERROR_MESSAGE);
        }else{
        try{
            switch (jButton3.getText()) {
                case "Save":
                    Sport_Event SE = new Sport_Event((String)SPpercent.getSelectedItem(),
                            SPname.getText(),(String)SPstatus.getSelectedItem(),
                            (String)SPtype.getSelectedItem(),Sched,new query_Date().query_Date(),
                            Integer.parseInt(administrator.userID.getText()));
                    Add_Sports_Event ASE = new Add_Sports_Event();
                    ASE.EventAdd(SE);
                    break;
                case "Update":
                     Update_Sport US = new Update_Sports_Event();
                     US.UpdateSport(Integer.parseInt(spID.getText()),Integer.parseInt(administrator.userID.getText()),(String)SPpercent.getSelectedItem(),
                            SPname.getText(),(String)SPstatus.getSelectedItem(),
                            (String)SPtype.getSelectedItem(),Sched,new query_Date().query_Date());
                     JOptionPane.showMessageDialog(null,"Succesfully Updated");
                     dispose();
                    break;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

