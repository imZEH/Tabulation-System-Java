    private void HourKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_HourKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE)||(c == KeyEvent.VK_PERIOD))) {
            getToolkit().beep();
            evt.consume();
        } else {
            // msg.setText("Can't Typed Char");
        }
        
         String pass = Hour.getText();
      int length;
      length = pass.length();
      if(length==2){
         getToolkit().beep();
            evt.consume();
      }
      else {}
    }//GEN-LAST:event_HourKeyTyped

