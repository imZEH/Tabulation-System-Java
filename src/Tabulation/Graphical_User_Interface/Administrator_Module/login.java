    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       List<Accounts> sList = new ArrayList<>();   
        login_class p =new login_class();
        Present_Judge PJ = new Imp_PresentJudge();
        try {
           sList= p.queryAccount(username.getText(), password.getText());
          String st = null;
          String type = null;
          String Name = null;
          int ID = 0;
            for (int i=0;i<sList.size();i++){
                ID = sList.get(i).getAcc_ID();
                Name = sList.get(i).getFullName();
                type = sList.get(i).getType();
                 st = sList.get(i).getStatus();
            }
            if((st.equals("Active"))&&(type.equals("Administrator"))){
                        dispose();
                        new administrator().show();
                        administrator.admin.setText(Name);
                        administrator.userID.setText(Integer.toString(ID));
                }else if((st.equals("Active"))&&(type.equals("Cultural Judges"))){
                        tempDB db  = new tempDDB_implementation();
                       db.add("1", Integer.toString(ID));
                        dispose();
                        new Scorer().show();
                        Scorer.judgname.setText(Name);
                       Scorer.judgeid.setText(Integer.toString(ID));
                       PJ.add(ID,"LOGIN");
                     //  new Scorer().setTitle(type);
                }
                else if((st.equals("Active"))&&(type.equals("Sports Judges"))){
                    dispose();
                    new Sport_window().show();
                    Sport_window.culturalname1.setText(Name);
                    Sport_window.cuturalid1.setText(Integer.toString(ID));
                }
                else{
                    JOptionPane.showMessageDialog(null,"Check your Username and Password","Error",JOptionPane.ERROR_MESSAGE);
                }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Check your Username and Password","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

