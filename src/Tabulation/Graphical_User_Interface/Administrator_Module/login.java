    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if(Address.getText().equals("")||(usern.getText().equals("")||(passw.getText().equals("")))){
          JOptionPane.showMessageDialog(null,"Connection failed"); 
       }
       
       else{
    	Properties prop = new Properties();
        
    	try {
    	
            String driver = "com.mysql.jdbc.Driver";
            prop.setProperty("driver", driver);
            prop.setProperty("address", Address.getText());
    	    prop.setProperty("username", usern.getText());
            prop.setProperty("password",passw.getText());
            
                
                prop.store(new FileOutputStream("config.properties"), null);
                JOptionPane.showMessageDialog(null,"Connection Successfully Configure");
                jCheckBox1.setSelected(false);
                this.setSize(555, 465);
    	} catch (IOException ex) {
    		ex.printStackTrace();
        }
       }
    }//GEN-LAST:event_jButton3ActionPerformed

